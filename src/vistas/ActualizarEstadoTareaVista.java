    private void btnActualizarTareasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarTareasActionPerformed
        // TODO add your handling code here:
        if(isANumber(jtfIdTarea.getText()) && !(jtfIdTarea.getText().equals(""))){
            TareaData td = new TareaData();
            int idTarea = Integer.parseInt(jtfIdTarea.getText());
            Tarea tarea = td.buscarTareaXiD(idTarea);
            int estado = jcboxEstado.getSelectedIndex();
            td.actualizarEstadoDeLasTareas(tarea, estado);
        }else{
            JOptionPane.showMessageDialog(this,"El campo idTarea debe ser un numero y debe tener contenido...");
        }
    }//GEN-LAST:event_btnActualizarTareasActionPerformed
    
    public boolean isANumber(String numero){ //Implemento este metodo para comprobar si un campo es un número o no...
        boolean es;
        try{
            Integer.parseInt(numero);
            es = true;
        }catch(NumberFormatException nfe){
            es = false;
        }
        return es;
    }

